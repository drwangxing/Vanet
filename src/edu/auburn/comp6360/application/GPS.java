package edu.auburn.comp6360.application;

public class GPS {
	
	private double x;
	private double y;
	private long timestamp;
	
	public void setX(double x) {
		this.x = x;
	}
	
	public void setY(double y) {
		this.y = y;
	}
	
	public double getX() {
		return this.x;
	}
	
	public double getY() {
		return this.y;
	}
	
	public long getTimestamp() {
		return this.timestamp;
	}
	

}
